// NSString + mySort.h
@interface NSString (sort)
- (NSComparisonResult)sort:(NSString *)str;
@end

@implementation NSString (sort)
- (NSComparisonResult)sort:(NSString *)str {
    NSString*s = [SHChineseSortSetting share].specialCharSectionTitle;
    BOOL b = [SHChineseSortSetting share].specialCharPositionIsFront;
    if ([self isEqualToString:s]){
        //相同
        if ([str isEqualToString:s]) {
            return NSOrderedSame;
        }
        return b ? NSOrderedAscending : NSOrderedDescending;
    }else if ([str isEqualToString:s]){
        return b ? NSOrderedDescending : NSOrderedAscending;
    }else{
        return [self localizedStandardCompare:str];
    }
}
@end


@implementation SHChineseSortSetting

+(SHChineseSortSetting*)share{
    static SHChineseSortSetting * singleton = nil ;
    if (singleton == nil) {
        singleton = [[SHChineseSortSetting alloc] init];
    }
    return singleton;
}

- (instancetype)init{
    if (self = [super init]) {
        _specialCharSectionTitle = @"#";
        _specialCharPositionIsFront = NO;
        _ignoreModelWithPrefix = @"1";
        _polyphoneMapping = [NSMutableDictionary dictionaryWithDictionary:
                             @{@"长":@"C",
                               @"沈":@"S",
                               @"中":@"Z",
                               @"会":@"H"
                               }];
    }
    return self;
}


-(void)setPolyphoneMapping:(NSMutableDictionary *)polyphoneMapping{
    [_polyphoneMapping addEntriesFromDictionary:polyphoneMapping];
}

@end

